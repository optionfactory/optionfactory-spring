package net.optionfactory.spring.downstream.plugin.gen;

import com.palantir.javapoet.ArrayTypeName;
import com.palantir.javapoet.ClassName;
import com.palantir.javapoet.ParameterizedTypeName;
import com.palantir.javapoet.TypeName;
import com.palantir.javapoet.TypeVariableName;

import java.io.File;
import java.io.FileWriter;
import java.io.IOException;
import java.lang.reflect.Modifier;
import java.lang.reflect.TypeVariable;
import java.nio.charset.StandardCharsets;
import java.util.ArrayList;
import java.util.Arrays;
import java.util.Collection;
import java.util.List;
import java.util.Map;
import java.util.stream.Collectors;
import java.util.stream.Stream;

import net.optionfactory.spring.downstream.plugin.processing.PayloadsScanner.PayloadType;
import net.optionfactory.spring.downstream.plugin.processing.TypesRegistry;
import net.optionfactory.spring.downstream.plugin.processing.TypesTranslator;

public class TypeScriptSourcesGenerator implements SourcesGenerator {

    private final File outputDir;
    private final Map<String, String> translations;

    public TypeScriptSourcesGenerator(File outputDir, Map<String, String> translations) {
        this.outputDir = outputDir;
        this.translations = translations;
    }

    @Override
    public List<GenerateOutcome> generate(TypesRegistry types, SourcesClassLoader cl) throws IOException {
        if (!outputDir.exists()) {
            outputDir.mkdirs();
        }

        try (final var fw = new FileWriter(new File(outputDir, "spec.d.ts"), StandardCharsets.UTF_8)) {
            final var outcomes = new ArrayList<GenerateOutcome>();
            final var translator = new TypesTranslator(types, translations);

            fw.write("// Generated by downstream-maven-plugin\n");
            fw.write("export type LocalDate = string;\n");
            fw.write("export type Instant = string;\n");
            fw.write("export type Locale = string;\n\n");

            for (final var mapping : types.allMappings()) {
                final var clazz = mapping.getKey();
                final var spec = buildSpec(cl, types, translator, types.getType(clazz), clazz);
                outcomes.add(new GenerateOutcome(clazz.getSimpleName(), true));
                fw.write(spec);
                fw.write("\n");
            }
            return outcomes;
        }
    }

    private String buildSpec(SourcesClassLoader cl, TypesRegistry types, TypesTranslator translator, PayloadType type, Class<?> clazz) {
        return type == PayloadType.DTO ? buildDtoSpec(cl, types, translator, clazz) : buildEnumSpec(types, translator, clazz);
    }

    private String buildDtoSpec(SourcesClassLoader cl, TypesRegistry types, TypesTranslator translator, Class<?> dtoClass) {
        final String name = resolveClassName(types, dtoClass);

        final var typeParams = dtoClass.getTypeParameters().length == 0
                ? ""
                : "<%s>".formatted(
                Stream.of(dtoClass.getTypeParameters())
                        .map(TypeVariable::getName)
                        .collect(Collectors.joining(", "))
        );
        final var fields = new StringBuilder();

        for (final var field : dtoClass.getDeclaredFields()) {
            if (Modifier.isStatic(field.getModifiers()) || Modifier.isTransient(field.getModifiers())) {
                continue;
            }
            final var javaPoetType = translator.translate(field.getAnnotatedType());
            final var tsType = mapJavaPoetToTs(cl, types, javaPoetType);
            final var isOptional = !field.getType().isPrimitive() && (field.getType() == java.util.Optional.class || hasNullableAnnotation(field));
            fields.append(String.format("    %s%s: %s;\n", field.getName(), isOptional ? "?" : "", tsType));
        }

        return """
                export interface %s%s {
                %s}
                """.formatted(name, typeParams, fields.toString());
    }

    private String buildEnumSpec(TypesRegistry types, TypesTranslator translator, Class<?> enumClass) {
        final var name = resolveClassName(types, enumClass);
        final var constants = enumClass.getEnumConstants();

        if (constants == null || constants.length == 0) {
            return """
                    export type %s = never;
                    """.formatted(name);
        }

        final var typeValues = Arrays.stream(constants)
                .map(c -> "\"" + c.toString() + "\"")
                .collect(Collectors.joining("\n    | "));

        return """
                export type %s = %s;
                """.formatted(name, typeValues);
    }

    private String resolveClassName(TypesRegistry types, Class<?> clazz) {
        if (!types.isRegistered(clazz)) {
            final var sb = new StringBuilder();
            Class<?> current = clazz;
            while (current != null) {
                sb.insert(0, current.getSimpleName());
                current = current.getDeclaringClass();
            }
            return sb.toString();
        }
        return String.join("", types.getClassName(clazz).simpleNames());
    }

    private String mapJavaPoetToTs(SourcesClassLoader cl, TypesRegistry types, TypeName type) {
        if (type instanceof ParameterizedTypeName pt) {
            final var rawName = pt.rawType().reflectionName();

            if (rawName.equals("java.util.Optional")) {
                return mapJavaPoetToTs(cl, types, pt.typeArguments().get(0));
            }

            final var rawClass = cl.load(rawName);
            if (rawClass == null) {
                throw new RuntimeException("TypeScript generation failed: generic raw type class not found: " + rawName);
            }

            if (Collection.class.isAssignableFrom(rawClass)) {
                return mapJavaPoetToTs(cl, types, pt.typeArguments().get(0)) + "[]";
            }

            if (Map.class.isAssignableFrom(rawClass)) {
                final var keyTypeName = pt.typeArguments().get(0);
                String keyType = mapJavaPoetToTs(cl, types, keyTypeName);
                String valType = mapJavaPoetToTs(cl, types, pt.typeArguments().get(1));

                boolean validKey = keyType.equals("string") || keyType.equals("number")
                        || keyType.equals("LocalDate") || keyType.equals("Instant");

                if (!validKey && keyTypeName instanceof ClassName cn) {
                    Class<?> keyClass = cl.load(cn.reflectionName());

                    if (keyClass != null && (keyClass.isEnum() || (types.isRegistered(keyClass) && types.getType(keyClass) == PayloadType.ENUM))) {
                        validKey = true;
                    }
                }

                if (!validKey) {
                    keyType = "string";
                }
                return String.format("Record<%s, %s>", keyType, valType);
            }

            final var tsArgs = pt.typeArguments().stream()
                    .map(t -> mapJavaPoetToTs(cl, types, t))
                    .collect(Collectors.joining(", "));
            return mapJavaPoetToTs(cl, types, pt.rawType()) + "<" + tsArgs + ">";
        }

        if (type instanceof ArrayTypeName at) {
            return mapJavaPoetToTs(cl, types, at.componentType()) + "[]";
        }
        if (type instanceof TypeVariableName tv) {
            return tv.name();
        }
        if (type instanceof ClassName cn) {
            final String fqn = cn.reflectionName();

            if (translations.containsKey(fqn)) {
                return translations.get(fqn);
            }

            switch (fqn) {
                case "java.lang.String":
                    return "string";
                case "java.lang.Object":
                case "java.util.Optional":
                    return "any";
                case "int":
                case "long":
                case "double":
                case "float":
                case "short":
                case "java.lang.Integer":
                case "java.lang.Long":
                case "java.lang.Double":
                case "java.math.BigDecimal":
                    return "number";
                case "boolean":
                case "java.lang.Boolean":
                    return "boolean";
                case "java.time.LocalDate":
                    return "LocalDate";
                case "java.time.Instant":
                    return "Instant";
                default:
                    final var clazz = cl.load(fqn);
                    return clazz == null ? cn.simpleName() : resolveClassName(types, clazz);
            }
        }

        if (type instanceof TypeName cn) {
            return cn.equals(TypeName.BOOLEAN) ? "boolean" : "number";
        }
        return "any";
    }

    private boolean hasNullableAnnotation(java.lang.reflect.Field field) {
        for (var annotation : field.getAnnotations()) {
            if (annotation.annotationType().getSimpleName().equals("Nullable")) {
                return true;
            }
        }
        for (var annotation : field.getAnnotatedType().getAnnotations()) {
            if (annotation.annotationType().getSimpleName().equals("Nullable")) {
                return true;
            }
        }
        return false;
    }
}
